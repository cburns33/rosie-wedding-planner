import https from "https";
import { writeFileSync } from "fs";
import * as fontkit from "fontkit";

function fetchFont() {
  return new Promise((resolve, reject) => {
    https
      .get(
        "https://raw.githubusercontent.com/google/fonts/main/ofl/greatvibes/GreatVibes-Regular.ttf",
        { headers: { "User-Agent": "node" } },
        (res) => {
          const chunks = [];
          res.on("data", (c) => chunks.push(c));
          res.on("end", () => resolve(fontkit.create(Buffer.concat(chunks))));
          res.on("error", reject);
        }
      )
      .on("error", reject);
  });
}

function textToPaths(font, text, fontSize, baselineY = 160) {
  const scale = fontSize / font.unitsPerEm;
  const run = font.layout(text);
  let offsetX = 0;
  const glyphs = [];

  for (let i = 0; i < run.glyphs.length; i++) {
    const glyph = run.glyphs[i];
    const pos = run.positions[i];
    const x = offsetX + pos.xOffset * scale;
    const y = baselineY + pos.yOffset * scale;
    const cmds = [];

    for (const cmd of glyph.path.commands) {
      if (cmd.command === "moveTo") {
        cmds.push({ c: "M", a: [cmd.args[0] * scale + x, -cmd.args[1] * scale + y] });
      } else if (cmd.command === "lineTo") {
        cmds.push({ c: "L", a: [cmd.args[0] * scale + x, -cmd.args[1] * scale + y] });
      } else if (cmd.command === "quadraticCurveTo") {
        cmds.push({
          c: "Q",
          a: [
            cmd.args[0] * scale + x,
            -cmd.args[1] * scale + y,
            cmd.args[2] * scale + x,
            -cmd.args[3] * scale + y,
          ],
        });
      } else if (cmd.command === "bezierCurveTo") {
        cmds.push({
          c: "C",
          a: [
            cmd.args[0] * scale + x,
            -cmd.args[1] * scale + y,
            cmd.args[2] * scale + x,
            -cmd.args[3] * scale + y,
            cmd.args[4] * scale + x,
            -cmd.args[5] * scale + y,
          ],
        });
      } else if (cmd.command === "closePath") {
        cmds.push({ c: "Z", a: [] });
      }
    }

    const d = cmds.map(({ c, a }) => c + a.map((n) => (+n).toFixed(2)).join(" ")).join(" ");
    glyphs.push({ id: `g${i}`, char: text[i] ?? "", d });
    offsetX += pos.xAdvance * scale;
  }

  let minX = Infinity;
  let minY = Infinity;
  let maxX = -Infinity;
  let maxY = -Infinity;

  for (const { d } of glyphs) {
    const nums = d.match(/-?\d+\.\d+/g)?.map(Number) ?? [];
    for (let i = 0; i < nums.length; i += 2) {
      minX = Math.min(minX, nums[i]);
      maxX = Math.max(maxX, nums[i]);
      minY = Math.min(minY, nums[i + 1]);
      maxY = Math.max(maxY, nums[i + 1]);
    }
  }

  const pad = 16;
  const viewBox = [
    minX - pad,
    minY - pad,
    maxX - minX + pad * 2,
    maxY - minY + pad * 2,
  ].map((n) => +n.toFixed(1));

  return { glyphs, viewBox: viewBox.join(" ") };
}

const font = await fetchFont();
const specs = [
  ["rosie", "Rosie", 200],
  ["forKelsie", "For Kelsie", 110],
  ["companion", "your planning companion", 72],
  ["spring2027", "Spring 2027", 64],
];

const out = {};
for (const [key, text, size] of specs) {
  out[key] = textToPaths(font, text, size);
}

const ts = `// Auto-generated by scripts/generate-mockup-paths.mjs — placeholder until custom handwriting SVGs ship.

export type MockupGlyph = { id: string; char: string; d: string };

export type MockupTextPaths = {
  glyphs: MockupGlyph[];
  viewBox: string;
};

export const MOCKUP_PATHS: Record<string, MockupTextPaths> = ${JSON.stringify(out, null, 2)} as const;
`;

writeFileSync("lib/mockup-paths.ts", ts);
console.log("Wrote lib/mockup-paths.ts");
